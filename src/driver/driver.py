def driver(mod: tvm.ir.IRModule, 
           params: Dict[str, tvm.nd.array],
           run: bool = False, build_dir: pathlib.Path = "build",
           byoc_path: pathlib.Path = ".", 
           no_of_inputs = 1,
           compare_with_x86: bool = False,
           out_channels: int = 1,
           out_height: int = 1,
           out_width: int = 1,
           profiling: list[str] = ['correct']):
    """
    Compile (and run) a model for DIANA for testing purposes

    If the run argument is used, then it will also run the compiled model
    on a remote GDB instance over port 3333.
    Afterwards an x86 compiled model is used to compare outputs
    """
    # Create the model library format file and unpack
    breakpoint()
    d_gap9 = MatchDriver(mod, params, build_dir=build_dir,
                          byoc_path=byoc_path,
                          no_of_inputs= no_of_inputs)
    d_gap9.tvm_compile(fusion=True)
    d_gap9.add_profiler(measurement="individual")
    d_gap9.gcc_compile(gcc_opt=3)
    output_pulp={'latencies':{0:-1},'output':[-1,-1,-1,-1],'correct':False}
    # Make for GAP9
    if run:
        # Run the binary on GAP9
        output_pulp = d_gap9.run()
        output_pulp['correct']=str(True)
        #d_gap9.process_profile()
    if compare_with_x86:
        # Compile and run the network on x86
        d_x86 = X86Driver(mod, params, build_dir, byoc_path)
        d_x86.tvm_compile(fusion=False)
        d_x86.gcc_compile(gcc_opt=0)
        output_x86 = d_x86.run()
        # Compare X86 and DIANA outputs
        # Use allclose, not allequal (in case of floats)
        output_pulp['correct']=str(np.ma.allclose(output_x86.reshape(int(out_channels),int(out_height),int(out_width)).transpose(1,2,0).flatten().astype(np.uint8),np.asarray(output_pulp['output']).astype(np.uint8)))
        if output_pulp['correct']=='False':
            output_pulp['correct_output']=output_x86.reshape(int(out_channels),int(out_height),int(out_width)).transpose(1,2,0).flatten().astype(np.uint8).tolist()
    retval=dict()
    for key in set(profiling+['latencies']):
        retval[key]=output_pulp[key]
    return retval